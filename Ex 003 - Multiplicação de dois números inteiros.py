''' Desenvolva um programa em Python para representar um algoritmo que
efetue a multiplicação de dois inteiros quaisquer.'''

print("Digite o primeiro número: ")
numero1 = int(input())
print("Digite o segundo número: ")
numero2 = int(input())

resultadoMultiplicacao = numero1 * numero2
print(f"A multiplicação entre {numero1} e {numero2} é {resultadoMultiplicacao}")
